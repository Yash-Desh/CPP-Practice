/*

Using 'cout' & new line characters in C++ 

*/
#include <iostream>
using namespace std;
int main()
{
    // There are 2 ways to go to a new line in C++

    //  1. using endl
    cout<<"first line "<<endl;
    cout<<"second line"<<endl;

    // 2. using \n
    cout<<"third line \n";
    cout<<"fourth line";



    return 0;
}