// Q. Program to check if a number is prime or not

#include <iostream>
using namespace std;

bool isPrime(int num)
{
    // Guard: 0, 1 and negative numbers are not prime.
    if(num < 2)
    {
        return false;
    }

    // How is num = 2 being handled correctly here ? 
    // for: num = 2 `factor < num` is false, so the for-loop exits.
    for(int factor = 2; factor < num; factor++)
    {
        if(num % factor == 0)
        {
            return false;
        }
    }
    return true;
}

int main()
{
    int num;
    cout<<"Enter a number : ";
    cin>>num;

    if(isPrime(num))
    {
        cout<<num<<" is Prime"<<endl;
    }
    else
    {
        cout<<num<<" is Non Prime"<<endl;
    }

    return 0;
}

